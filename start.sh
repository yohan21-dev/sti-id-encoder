#!/bin/bash
# ============================================================
#  STI College Cubao вЂ” ID Management System
#  One-command startup script
# ============================================================

ROOT="$(cd "$(dirname "$0")" && pwd)"
BACKEND="$ROOT/backend"
FRONTEND="$ROOT/frontend"
PID_FILE="$ROOT/.pids"

RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'; BLUE='\033[0;34m'; NC='\033[0m'

stop_all() {
  echo -e "\n${YELLOW}Shutting down...${NC}"
  if [ -f "$PID_FILE" ]; then
    while IFS= read -r pid; do
      kill "$pid" 2>/dev/null
    done < "$PID_FILE"
    rm -f "$PID_FILE"
  fi
  exit 0
}

trap stop_all SIGINT SIGTERM

echo ""
echo -e "${BLUE}в•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—${NC}"
echo -e "${BLUE}в•‘  STI College Cubao вЂ” ID Management       в•‘${NC}"
echo -e "${BLUE}в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ${NC}"
echo ""

# в”Ђв”Ђ Check Python в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
if ! command -v python3 &>/dev/null; then
  echo -e "${RED}вњ— python3 not found. Please install Python 3.9+${NC}"
  exit 1
fi

# в”Ђв”Ђ Check Flask в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo -e "${YELLOW}в–є Checking Python dependencies...${NC}"
python3 -c "import flask, flask_sock, flask_cors" 2>/dev/null
if [ $? -ne 0 ]; then
  echo -e "${YELLOW}  Installing: flask flask-sock flask-cors${NC}"
  pip install flask flask-sock flask-cors --break-system-packages -q \
    || pip install flask flask-sock flask-cors -q
fi
echo -e "${GREEN}  вњ“ Python dependencies OK${NC}"

# в”Ђв”Ђ Check Node в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
if ! command -v node &>/dev/null; then
  echo -e "${RED}вњ— Node.js not found. Please install Node 18+${NC}"
  exit 1
fi

# в”Ђв”Ђ Install frontend deps if needed в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
if [ ! -d "$FRONTEND/node_modules" ]; then
  echo -e "${YELLOW}в–є Installing frontend dependencies...${NC}"
  cd "$FRONTEND" && npm install --silent
  echo -e "${GREEN}  вњ“ npm install done${NC}"
fi

echo ""

# в”Ђв”Ђ Start Backend в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo -e "${YELLOW}в–є Starting Flask backend on http://localhost:5000 ...${NC}"
cd "$BACKEND"
python3 server.py > "$ROOT/backend.log" 2>&1 &
BACKEND_PID=$!
echo "$BACKEND_PID" > "$PID_FILE"
sleep 2

if ! kill -0 $BACKEND_PID 2>/dev/null; then
  echo -e "${RED}вњ— Backend failed to start. Check backend.log${NC}"
  cat "$ROOT/backend.log"
  exit 1
fi
echo -e "${GREEN}  вњ“ Backend running (PID $BACKEND_PID)${NC}"

# в”Ђв”Ђ Start Frontend в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo -e "${YELLOW}в–є Starting Vite frontend on http://localhost:5173 ...${NC}"
cd "$FRONTEND"
npm run dev -- --host 2>&1 &
FRONTEND_PID=$!
echo "$FRONTEND_PID" >> "$PID_FILE"
sleep 3

echo ""
echo -e "${GREEN}в•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—${NC}"
echo -e "${GREEN}в•‘  вњ“  SYSTEM RUNNING                       в•‘${NC}"
echo -e "${GREEN}в•‘                                          в•‘${NC}"
echo -e "${GREEN}в•‘  App  в†’  http://localhost:5173           в•‘${NC}"
echo -e "${GREEN}в•‘  API  в†’  http://localhost:5000/api       в•‘${NC}"
echo -e "${GREEN}в•‘  WS   в†’  ws://localhost:5173/ws          в•‘${NC}"
echo -e "${GREEN}в•‘                                          в•‘${NC}"
echo -e "${GREEN}в•‘  Press Ctrl+C to stop                    в•‘${NC}"
echo -e "${GREEN}в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ${NC}"
echo ""

# в”Ђв”Ђ Keep running в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
wait $FRONTEND_PID